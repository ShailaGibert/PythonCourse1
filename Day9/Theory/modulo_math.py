# Tiene muchísimas funcionalidades
import math

resultado = math.floor(2.9530)  # Trunca el valor pasado como argumento
print(resultado)

resultado = math.ceil(2.50)  # Trunca el valor hacia arriba
print(resultado)

resultado = round(2.50)  # Recordemos que para redondear es round(), que no viene del módulo math
print(resultado)

